# Convert String to Integer
num_str="56"                           # A number stored as a string
num_int = int(num_str)                 # Convert string to integer using int()
print("String to Integer :",num_int)

# Convert Integer to Float
int_num = 12                           # Integer value
float_num = float(int_num)             # Convert integer to float using float()
print("Integer to Float : ",float_num)

# Convert Float to String
float = 78.9                           # Float value
string = str(float)                    # Convert float to string using str()
print("Float to String : ",string)